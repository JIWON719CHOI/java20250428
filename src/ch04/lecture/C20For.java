package ch04.lecture;

public class C20For {
    public static void main(String[] args) {
        System.out.println("코드1");
        System.out.println("코드2");
        // 빨간 점은 break point 여기서 디버그 실행하면..
        // 일시정지 하고 f8누르면 하나씩 다음 줄 실행

        for (int i = 0; i < 5; i++) {
            System.out.println(i);
        }
    }
}
