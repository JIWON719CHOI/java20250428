package ch02.lecture;

public class C09String {
    public static void main(String[] args) {
        // string 문자열 :
        // literal : 큰따옴표 사용하세요.

        String a;
        a = "hello";
        System.out.println("a = " + a);
        a = "안녕하시소~";
        System.out.println("a = " + a);
        a = "😒💖❌💥🔮🌀";
        System.out.println("a = " + a);
        a = ""; // 빈 문자열도 가능~~
    }
}
